     private void loadMa(){
         jComboBox2.removeAllItems();
         List<KhachHang> l1 = iofile.doc("src/controller/kh.dat");
         for (KhachHang i : l1) {
             jComboBox2.addItem(i.getMa() + "");
         }
         jComboBox3.removeAllItems();
         List<NganHang> l2 = iofile.doc("src/controller/nh.dat");
         for (NganHang i : l2) {
             jComboBox3.addItem(i.getMa() + "");
         }
     }
     
     private KhachHang getKhachHang(int ma) {
         List<KhachHang> l1 = iofile.doc("src/controller/kh.dat");
         for (KhachHang i : l1) {
             if (ma == i.getMa())
                 return i;
         }
         return null;
     }
     
     private NganHang getNganHang(int ma) {
         List<NganHang> l2 = iofile.doc("src/controller/nh.dat");
         for (NganHang i : l2) {
             if (ma == i.getMa()) {
                 return i;
             }
         }
         return null;
     }
     
     private List<SoTietKiem> getAll() {
         List<SoTietKiem> list = new ArrayList<>();
        for (int i = 0; i < d.getRowCount(); i++) {
            int makh = Integer.parseInt(d.getValueAt(i, 0).toString());
            int manh = Integer.parseInt(d.getValueAt(i, 2).toString());
            double tiengui = Double.parseDouble(d.getValueAt(i, 4).toString());
            int kihan = Integer.parseInt(d.getValueAt(i, 5).toString());
            String ngaygui = d.getValueAt(i, 6).toString();
            
            list.add(new SoTietKiem(getKhachHang(makh), getNganHang(manh), tiengui, kihan, ngaygui));
        }
        return list;
     }
     
