    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed

        List<SoTietKiem> list = getAll();
        if (jComboBox1.getSelectedIndex() == 0) {
            list.sort(new Comparator<SoTietKiem>() {
                public int compare(SoTietKiem d1, SoTietKiem d2) {
                    String t1 = d1.getK().getTen();
                    String t2 = d2.getK().getTen();
                    String ten1 = t1.substring(t1.lastIndexOf(" "), t1.length()) + t1;
                    String ten2 = t2.substring(t2.lastIndexOf(" "), t2.length()) + t2;
                    return ten1.compareToIgnoreCase(ten2);
                }
            });
        }
        
        if (jComboBox1.getSelectedIndex() == 1) {
            list.sort(new Comparator<SoTietKiem>() {
                public int compare(SoTietKiem d1, SoTietKiem d2) {
                    return (int)(d1.getTiengui()- d2.getTiengui());
                }
            });
        }
        d.setRowCount(0);
        for (SoTietKiem i : list) {
            d.addRow(i.toObject());
        }
    }//GEN-LAST:event_jButton8ActionPerformed


