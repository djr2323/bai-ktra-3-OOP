    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        List<KhachHang> list = new ArrayList<>();
        for (int i = 0; i < d.getRowCount(); i++) {
            int ma = Integer.parseInt(d.getValueAt(i, 0).toString());
            String ten = d.getValueAt(i, 1).toString();
            String diachi = d.getValueAt(i, 2).toString();
            String loai = d.getValueAt(i, 3).toString();
         

            list.add(new KhachHang(ma, ten, diachi, loai));
        }
        iofile.viet(file, list);

    }//GEN-LAST:event_jButton5ActionPerformed

