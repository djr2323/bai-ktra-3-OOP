    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        btfalse();
        them = true;
        File f = new File(file);
        if (f.exists()) {
            List<KhachHang> list = iofile.doc(file);
            if (list.size() > 0) {
                KhachHang.setMast(list.get(list.size() - 1).getMa() + 1);
            }
        }
        jTextField1.setText(KhachHang.getMast() + "");
        jTextField2.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (them) {
            KhachHang c = new KhachHang();
            c.setTen(jTextField2.getText());
            c.setDc(jTextField3.getText());
            c.setLoai(jComboBox1.getSelectedItem().toString());
            d.addRow(c.toObject());
            bttrue();
            them = false;
        }
        if (sua) {
            int r = jTable1.getSelectedRow();
            d.setValueAt(jTextField2.getText(), r, 1);
            d.setValueAt(jTextField3.getText(), r, 2);
            d.setValueAt(jComboBox1.getSelectedItem().toString(), r, 3);
            sua = false;
            bttrue();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        if (r < 0 || r > d.getRowCount() - 1) {
            JOptionPane.showMessageDialog(this, "Chua chon");
        } else {
            sua = true;
            btfalse();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        jTextField1.setText(d.getValueAt(r, 0).toString());
        jTextField2.setText(d.getValueAt(r, 1).toString());
        jTextField3.setText(d.getValueAt(r, 2).toString());
         for (int i = 0; i < jComboBox1.getItemCount(); i++) {
            if (jComboBox1.getSelectedItem().toString().equalsIgnoreCase(d.getValueAt(r, 3).toString())){
                jComboBox1.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked


